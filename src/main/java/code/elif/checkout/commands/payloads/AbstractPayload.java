package code.elif.checkout.commands.payloads;

/**
 * A marker interface representing a generic payload.
 * <p>
 * Implementations of this interface can be used to define
 * different types of payloads in the application.
 * </p>
 * <p>
 * This interface does not define any methods and serves only to
 * mark a class as a type of payload.
 * </p>
 * <p>
 * Example usage:
 * </p>
 * <pre>
 * public class AddItemPayload implements AbstractPayload {
 *     // Class implementation
 * }
 * </pre>
 *
 * @see AddItemPayload
 * @since 1.0
 */
public interface AbstractPayload {
}
